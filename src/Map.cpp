Map::Map() {
    block = Game::loadTexture("../resources/img/blue.png");
    nonBlock = Game::loadTexture("../resources/img/black.png");


    loadMap(levelOne);

    m_sourceRectangle.x = m_sourceRectangle.y = 0;
    m_sourceRectangle.w = m_destinationRectangle.w = 50;
    m_sourceRectangle.h = m_destinationRectangle.h = 50;

    m_destinationRectangle.x = m_destinationRectangle.y = 0;


}

void Map::loadMap(int array[20][20]) {
    for (int row = 0; row < 20; row++) {
        for (int column = 0; column < 20; column++) {
            map2D[row][column] = array[row][column];
        }
    }
}

void Map::drawMap() {
    int type = 0;


    for (int row = 0; row < 20; row++) {
        for (int column = 0; column < 20; column++) {
            type = map2D[row][column];

            m_destinationRectangle.x = column * 32;
            m_destinationRectangle.y = row * 32;

            if (type == 1) {
                TextureManager::draw(block, m_sourceRectangle, m_destinationRectangle);
            } else {
                TextureManager::draw(nonBlock, m_sourceRectangle, m_destinationRectangle);
            }

        }
    }
}


